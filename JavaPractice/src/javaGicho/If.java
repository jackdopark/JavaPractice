package javaGicho;

public class If {

	public static void main(String[] args) {
		
		//if문에 대해
		
		/* if( 조건문(식)){실행 코드}
		 * 조건문은 결과가 참 또는 거짓으로 나와야 한다.
		 * 
		 */
		
		int a = 10;
		
		if(a>5) {
			System.out.println("a는 5보다 크다."); 
			//조건이 맞으면 출력이 되고 안맞으면 바로 if문은 끝난다.
		}
		
		System.out.println("종료");
		
		
	}

}
